mark = int(input("Enter your marks: "))

if mark >= 80 and mark <= 100:
    print("You have an A")
elif mark >= 60 and mark <= 79:
    print("You have an B")
elif mark >= 40 and mark <= 59:
    print ("You have an C")
elif mark >= 30 and mark <= 39:
    print("FAILURE")
else:
    print("Wrong input, check your marks")




























































































































































































































































































































































































































































































