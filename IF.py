num= int(input("Enter your number:"))

if num >= 8 and num <= 10:
    print("Large number")
elif num >= 6 and num <= 7:
    print("fair number")
elif num >= 4 and num <= 5:
    print ("medium number")
elif num >= 2 and num <= 3:
    print("small number")
else:
    print("Wrong input, check your marks")
